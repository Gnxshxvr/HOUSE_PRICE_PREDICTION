# =======================
# IMPORT REQUIRED LIBRARIES
# =======================

import pandas as pd  # pandas is used for handling datasets (tables, CSV files, etc.)

# =======================
# LOAD DATASET
# =======================

data = pd.read_csv("House Price India.csv")
# reads the CSV file and stores it in a DataFrame called 'data'

data.head()
# displays first 5 rows of the dataset (default = 5)
# useful to quickly understand structure of dataset

# =======================
# BASIC DATA EXPLORATION
# =======================

data.info()
# shows:
# - column names
# - number of non-null values
# - data types (int, float, object, etc.)

data.columns
# returns all column names

data.describe()
# gives statistical summary:
# mean, std, min, max, quartiles etc.

# =======================
# PRICE COLUMN ANALYSIS
# =======================

stats = data['Price'].describe().reset_index()
# describe price column and convert result into dataframe

stats["Price"] = round(stats["Price"])
# round values for better readability

stats
# display stats

# =======================
# DATA CLEANING
# =======================

data.isna().sum()
# checks how many missing (NaN) values in each column

data.duplicated().sum()
# checks number of duplicate rows

# if missing or duplicate data exists → remove them

data.dropna(inplace=True)
# removes rows with missing values

data.drop_duplicates(inplace=True)
# removes duplicate rows

# =======================
# DATA VISUALIZATION
# =======================

import matplotlib.pyplot as plt  # used for plotting graphs

data.groupby("condition of the house")['Price'].mean().sort_values(ascending=False).plot(kind="bar")
# Explanation:
# - group data by "condition of the house"
# - calculate average price for each condition
# - sort values in descending order
# - plot bar graph

plt.title("condition of the house")   # graph title
plt.ylabel("mean price")             # y-axis label
plt.xlabel("condition")              # x-axis label
plt.show()                           # display the graph

# =======================
# SELECT IMPORTANT FEATURES
# =======================

data[["number of bedrooms", "number of bathrooms"]]
# just previewing selected columns

# =======================
# DEFINE INPUT (X) AND OUTPUT (Y)
# =======================

x = data[[
    "number of bedrooms",
    "number of bathrooms",
    'living area',
    'condition of the house',
    "Number of schools nearby"
]]
# input features (independent variables)

y = data[["Price"]]
# target variable (dependent variable we want to predict)

x  # display X
y  # display Y

# =======================
# TRAIN-TEST SPLIT
# =======================

from sklearn.model_selection import train_test_split

x_train, x_test, y_train, y_test = train_test_split(x, y, test_size=0.2)
# split dataset into:
# - 80% training data
# - 20% testing data

# =======================
# DECISION TREE + GRID SEARCH
# =======================

from sklearn.model_selection import GridSearchCV
from sklearn.tree import DecisionTreeRegressor

# hyperparameter grid
param_grid = {
    "criterion": ["mse", "friedman_mse", "mae"],  # how split quality is measured
    "splitter": ["best", "random"],               # how splits are chosen
    "max_depth": [None, 10, 20, 30, 40, 50],     # max tree depth
    "min_samples_split": [2, 5, 10],              # min samples to split node
    "min_samples_leaf": [1, 2, 4]                # min samples at leaf node
}

tree_model = DecisionTreeRegressor()
# create decision tree model

grid_tree = GridSearchCV(tree_model, param_grid, cv=5)
# GridSearchCV tries all combinations of parameters
# cv=5 → 5-fold cross validation

grid_tree.fit(x_train, y_train)
# train model and find best parameters

grid_tree.best_params_
# shows best parameter combination

tree_preds = grid_tree.predict(x_test)
# predict house prices using decision tree

# =======================
# EVALUATION (DECISION TREE)
# =======================

from sklearn.metrics import mean_absolute_error

mean_absolute_error(y_test, tree_preds)
# MAE = average difference between predicted & actual values

# =======================
# LINEAR REGRESSION MODEL
# =======================

from sklearn.linear_model import LinearRegression

lr = LinearRegression()
# create linear regression model

lr.fit(x_train, y_train)
# train model

predslr = lr.predict(x_test)
# predict using linear regression

mean_absolute_error(y_test, predslr)
# evaluate model

# =======================
# RANDOM FOREST MODEL
# =======================

from sklearn.ensemble import RandomForestRegressor

rfr = RandomForestRegressor()
# ensemble model (multiple trees)

paramgridrfr = {
    "n_estimators": [5, 6, 7, 8, 9, 10],  # number of trees
    "max_depth": [5, 10, 15]             # depth of trees
}

gridrfr = GridSearchCV(rfr, paramgridrfr)
# grid search for best random forest params

gridrfr.fit(x_train, y_train)
# train model

gridrfr.best_params_
# best parameters

rfrpredictions = gridrfr.predict(x_test)
# predictions

mean_absolute_error(y_test, rfrpredictions)
# evaluation

gridrfr
# shows full grid search object

# =======================
# SAVE MODEL
# =======================

import joblib

joblib.dump(gridrfr, "HousePriceModel.pkl")
# saves trained model into file
# you can load it later using joblib.load()
